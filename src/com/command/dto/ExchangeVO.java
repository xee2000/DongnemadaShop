package com.command.dto;

public class ExchangeVO {

		private String exchange_code;
		private String exchange_status;
		private String exchange_reason;
		private int order_number;
		private int order_qty;
		private int order_allprice;
		
		
		public String getExchange_code() {
			return exchange_code;
		}
		public void setExchange_code(String exchange_code) {
			this.exchange_code = exchange_code;
		}
		public String getExchange_status() {
			return exchange_status;
		}
		public void setExchange_status(String exchange_status) {
			this.exchange_status = exchange_status;
		}
		public String getExchange_reason() {
			return exchange_reason;
		}
		public void setExchange_reason(String exchange_reason) {
			this.exchange_reason = exchange_reason;
		}
		public int getOrder_number() {
			return order_number;
		}
		public void setOrder_number(int order_number) {
			this.order_number = order_number;
		}
		public int getOrder_qty() {
			return order_qty;
		}
		public void setOrder_qty(int order_qty) {
			this.order_qty = order_qty;
		}
		public int getOrder_allprice() {
			return order_allprice;
		}
		public void setOrder_allprice(int order_allprice) {
			this.order_allprice = order_allprice;
		}
		
		

}
